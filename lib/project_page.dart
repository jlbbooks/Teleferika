              Tab(icon: Icon(Icons.info_outline), text: "Details"),
              Tab(
                icon: Icon(Icons.list_alt_outlined),
                text: "Points",
              ), // Changed icon for variety
              Tab(icon: Icon(Icons.explore_outlined), text: "Compass"),
              Tab(icon: Icon(Icons.map_outlined), text: "Map"),
            ],
          ),
        ),
        body: PopScope(
          // Use PopScope for "are you sure you want to leave" dialog
          canPop: false,
          onPopInvoked: (bool didPop) async {
            if (didPop) {
              return;
            }
            if (_hasUnsavedChanges) {
              final bool? shouldPop = await showDialog<bool>(
                context: context,
                builder: (context) => AlertDialog(
                  title: const Text('Unsaved Changes'),
                  content: const Text(
                    'You have unsaved changes. Do you want to discard them and leave?',
                  ),
                  actions: [
                    TextButton(
                      child: const Text('Cancel'),
                      onPressed: () => Navigator.of(context).pop(false),
                    ),
                    TextButton(
                      child: const Text('Discard'),
                      onPressed: () => Navigator.of(context).pop(true),
                    ),
                  ],
                ),
              );
              if (shouldPop ?? false) {
                if (mounted) Navigator.of(context).pop();
              }
            } else {
              // No unsaved changes, so we can pop.
              // Now check if a save occurred at any point.
              if (mounted) {
                if (_projectWasSuccessfullySaved) {
                  Navigator.of(context).pop({
                    'action': 'saved', // Or 'saved_and_exited'
                    'id': _currentProject.id, // Pass the latest saved state
                  });
                } else {
                  // No unsaved changes, and no save occurred during this page's lifetime
                  Navigator.of(
                    context,
                  ).pop(); //({'action': 'no_changes_made'}); // Or simply pop() for null
                }
              }
            }
          },
          child: TabBarView(
            children: [
              // Tab 1: Project Details Form
              SingleChildScrollView(
                padding: const EdgeInsets.all(16.0),
                child: Form(
                  key: _projectFormKey,
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.stretch,
                    children: <Widget>[
                      _buildTextFormField(
                        controller: _nameController,
                        label: 'Project Name',
                        validator: (value) {
                          if (value == null || value.trim().isEmpty) {
                            return 'Project name cannot be empty.';
                          }
                          return null;
                        },
                      ),
                      const SizedBox(height: 8),
                      InkWell(
                        onTap: () => _selectProjectDate(context),
                        child: InputDecorator(
                          decoration: const InputDecoration(
                            labelText: 'Project Date',
                            border: OutlineInputBorder(),
                          ),
                          child: Text(formattedProjectDate),
                        ),
                      ),
                      const SizedBox(height: 8),
                      _buildTextFormField(
                        controller: _noteController,
                        label: 'Notes',
                        maxLines: 3,
                      ),
                      const SizedBox(height: 8),
                      Row(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Expanded(
                            child: _buildTextFormField(
                              controller: _azimuthController,
                              label: 'Reference Azimuth (°)',
                              keyboardType:
                                  const TextInputType.numberWithOptions(
                                    decimal: true,
                                    signed: true,
                                  ),
                              validator: (value) {
                                if (value != null && value.trim().isNotEmpty) {
                                  final num = double.tryParse(value.trim());
                                  if (num == null) return 'Invalid number.';
                                  if (num <= -360 || num >= 360)
                                    return 'Must be +/-359.99';
                                }
                                return null;
                              },
                            ),
                          ),
                          const SizedBox(width: 10),
                          Padding(
                            padding: const EdgeInsets.only(top: 8.0),
                            child: ElevatedButton(
                              onPressed: _calculateAzimuth,
                              style: ElevatedButton.styleFrom(
                                padding: const EdgeInsets.symmetric(
                                  horizontal: 12,
                                  vertical: 12,
                                ),
                              ),
                              child: const Text("Calculate"),
                            ),
                          ),
                        ],
                      ),
                      const SizedBox(height: 16),
                      if (!_isEffectivelyNew && _lastUpdateTime != null)
                        Padding(
                          padding: const EdgeInsets.symmetric(vertical: 8.0),
                          child: Text(
                            'Last updated: $formattedLastUpdate',
                            style: Theme.of(context).textTheme.bodySmall,
                            textAlign: TextAlign.center,
                          ),
                        ),
                      // Add other relevant UI elements from your original design
                    ],
                  ),
                ),
              ),
              // Tab 2: Points
              PointsToolView(
                key: _pointsToolViewKey, // Assign the GlobalKey
                project: _currentProject,
                onPointsChanged: _onPointsChanged,
                newlyAddedPointId: _newlyAddedPointId,
              ),

              // Tab 3: Compass
              // You might need to pass project data or specific settings
              CompassToolView(
                project: _currentProject,
                onAddPointFromCompass:
                    _initiateAddPointFromCompass, // Pass the callback
                isAddingPoint: _isAddingPointFromCompassInProgress,
              ),

              // Tab 4: Map
              // Pass the current project. MapToolView will handle loading its points.
              Center(
                child: SizedBox(
                  height:
                      MediaQuery.of(context).size.height *
                      0.7, // e.g., 60% of screen height
                  child: MapToolView(project: _currentProject),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }

  void _onPointsChanged() async {
    logger.info(
      "ProjectDetailsPage: Points changed, reloading project details.",
    );
    await _loadProjectDetails(); // Reload project details to get new start/end IDs
    _pointsToolViewKey.currentState
        ?.refreshPoints(); // Ensure PointsToolView itself also refreshes its internal list
  }

  Widget _buildTextFormField({
    required TextEditingController controller,
    required String label,
    int maxLines = 1,
    TextInputType? keyboardType,
    String? Function(String?)? validator,
    AutovalidateMode autovalidateMode = AutovalidateMode.onUserInteraction,
    bool readOnly = false,
    Widget? suffixIcon,
    VoidCallback? onTap,
  }) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 8.0),
      child: TextFormField(
        controller: controller,
        decoration: InputDecoration(
          labelText: label,
          border: const OutlineInputBorder(),
          suffixIcon: suffixIcon,
          // contentPadding: const EdgeInsets.symmetric(
          //   horizontal: 14.0,
          //   vertical: 18.0,
          // ),
        ),
        style: const TextStyle(fontSize: 18.0),
        maxLines: maxLines,
        keyboardType: keyboardType,
        validator: validator,
        autovalidateMode: autovalidateMode,
        readOnly: readOnly,
        onTap: onTap,
      ),
    );
  }

  Widget _buildReadOnlyField(
    String label,
    String value, {
    TextStyle? textStyle,
    EdgeInsetsGeometry? contentPadding,
  }) {
    return InputDecorator(
      decoration: InputDecoration(
        labelText: label,
        border: const OutlineInputBorder(),
        contentPadding:
            contentPadding ??
            const EdgeInsets.symmetric(horizontal: 14.0, vertical: 18.0),
      ),
      child: Text(value, style: textStyle ?? const TextStyle(fontSize: 18.0)),
    );
  }

  Widget _majorActionButton(
    IconData icon,
    String label,
    VoidCallback onPressed,
  ) {
    return ElevatedButton.icon(
      icon: Icon(icon),
      label: Text(label),
      style: ElevatedButton.styleFrom(
        padding: const EdgeInsets.symmetric(vertical: 16.0),
        textStyle: const TextStyle(fontSize: 16.0, fontWeight: FontWeight.bold),
      ),
      onPressed: onPressed,
    );
  }
