  /// Get English country name from localized name (reverse lookup)
  static String? getEnglishCountry(
    BuildContext context,
    String localizedCountry,
  ) {
    final locale = Localizations.localeOf(context);
    if (locale.languageCode == 'it') {
      // Reverse lookup in Italian map
      for (final entry in countriesIt.entries) {
        if (entry.value == localizedCountry) {
          return entry.key;
        }
      }
    }
    return localizedCountry; // Already English or not found
  }

