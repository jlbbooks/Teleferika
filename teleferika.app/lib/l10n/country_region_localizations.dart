  static String getLocalizedCountry(BuildContext context, String country) {
    final locale = Localizations.localeOf(context);
    if (locale.languageCode == 'it') {
      return countriesIt[country] ?? country;
    }
    return country; // Default to English
  }

  /// Get localized region/state name
  static String getLocalizedRegion(
    BuildContext context,
    String country,
    String region,
  ) {
    final locale = Localizations.localeOf(context);
    if (locale.languageCode == 'it') {
      final countryRegions = regionsIt[country];
      if (countryRegions != null) {
        return countryRegions[region] ?? region;
      }
    }
    return region; // Default to English
  }

